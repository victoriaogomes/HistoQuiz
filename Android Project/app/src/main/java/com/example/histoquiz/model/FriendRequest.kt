package com.example.histoquiz.model

/**
 * Classe utilizada para armazenar as informações necessárias correspondentes a uma solicitação de
 * amizade
 */
class FriendRequest
(
    /**
     * Método utilizado para modificar o nome desse amigo
     * @param name - novo nome
     */
